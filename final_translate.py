def should_skip_translation(line):
    """Check if line should not be translated (code blocks, URLs, etc.)"""
    # Skip Python code keywords
    python_keywords = ['def ', 'class ', 'import ', 'from ', 'return ', 
                       'if ', 'else:', 'elif ', 'for ', 'while ', 
                       'try:', 'except:', 'finally:', 'with ', 'async def',
                       'await ', 'yield ', 'lambda ', '@']
    
    for keyword in python_keywords:
        if keyword in line:
            return True
    
    # Skip URLs
    if 'http://' in line or 'https://' in line or 'href=' in line or 'src=' in line:
        return True
    
    # Skip HTML/CSS
    if line.strip().startswith(('.', '#', 'background', 'color:', 'font-', 'padding', 'margin')):
        return True
        
    return False


def translate_python_comments(text):
    """Translate Python comments (lines starting with #)"""
    lines = text.split('\n')
    translated_lines = []
    
    for line in lines:
        stripped = line.lstrip()
        if stripped.startswith('#'):
            # This is a comment, translate it
            indent = line[:len(line) - len(stripped)]
            comment = stripped[1:].strip()
            
            # Apply translations to comment
            translated_comment = comment
            for ru, uk in TRANSLATIONS.items():
                if ru in translated_comment:
                    translated_comment = translated_comment.replace(ru, uk)
            
            translated_lines.append(f"{indent}# {translated_comment}")
        else:
            translated_lines.append(line)
    
    return '\n'.join(translated_lines)


def translate_text(text):
    """Apply all translations to text while preserving code and structure"""
    # First translate Python comments in <code> blocks
    if '<code' in text and '</code>' in text:
        # Extract and translate code blocks
        def translate_code_block(match):
            code = match.group(0)
            return translate_python_comments(code)
        
        text = re.sub(r'<code[^>]*>.*?</code>', translate_code_block, text, flags=re.DOTALL)
    
    # Sort translations by length (longest first) to avoid partial replacements
    sorted_translations = sorted(TRANSLATIONS.items(), key=lambda x: len(x[0]), reverse=True)
    
    # Apply translations
    for russian, ukrainian in sorted_translations:
        # Use word boundaries for better matching, but handle special cases
        if russian.strip() != russian:  # Has spaces (prepositions/conjunctions)
            text = text.replace(russian, ukrainian)
        else:
            # For words, use regex with word boundaries
            pattern = re.compile(r'\b' + re.escape(russian) + r'\b')
            text = pattern.sub(ukrainian, text)
    
    return text


